package br.com.fiap.tdst.am.advocacia.modelo;

public class FonePessoa {

	private int cdPessoa;
	private int cdFone;
	private int nrRamal;
	
	
	public int getCdPessoa() {
		return cdPessoa;
	}
	public void setCdPessoa(int cdPessoa) {
		this.cdPessoa = cdPessoa;
	}
	public int getCdFone() {
		return cdFone;
	}
	public void setCdFone(int cdFone) {
		this.cdFone = cdFone;
	}
	public int getNrRamal() {
		return nrRamal;
	}
	public void setNrRamal(int nrRamal) {
		this.nrRamal = nrRamal;
	}
	
	
}
